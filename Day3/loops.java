package Day3;

public class loops {
    public static void main(String[] args) {
        //By using for loop the the code will be run 5 times and print the values from the array
        int array[] = {1,2,3,4,5};
        for(int i=0; i<array.length; i++){
            System.out.print(array[i]);//12345
        }
    }
}
